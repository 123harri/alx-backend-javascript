import Car from './10-car';

class EVCar extends Car {
  constructor(brand, motor, color, range) {
    super(brand, motor, color);
    this._range = range;
  }

  get range() {
    return this._range;
  }

  cloneCar() {
    return new Car(); // Ensure cloneCar returns a new instance of Car
  }
}

export default EVCar;
